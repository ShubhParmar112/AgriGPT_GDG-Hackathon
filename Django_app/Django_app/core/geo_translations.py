def localize_options(values: list, translation_map: dict, lang_code: str) -> list:
    """Returns [(value, label), ...] pairs for a <select>: value stays the
    original English string (what the ML model expects), label is the
    translated display text for lang_code, falling back to the English
    value itself if no translation exists."""
    lookup = translation_map.get(lang_code, {})
    return [(v, lookup.get(v, v)) for v in values]
